package P3;

public class Person {
	private String name;
	
	/**
	 * 
	 * @param name the name of the person
	 */
	
	public Person(String name) {
		this.name = name;
	}
	
	/**
	 * 
	 *@description get the name of the person 
	 *@return the name of the person
	 */
	
	public String Name() {
		return name;
	}
}